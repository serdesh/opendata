        <p class="question"><strong>7. Что такое CSV и машиночитаемые данные?</strong></p>
        <blockquote>
        <p><strong>Машиночитаемые данные</strong>&nbsp;– данные, представленные в описанном формате, позволяющем информационным системам без участия человека идентифицировать, обрабатывать, преобразовывать такие данные и их составные части (элементы), а также обеспечивать доступ к ним.</p>
        <p><strong>CSV&nbsp;</strong>– (Comma-Separated Values — значения, разделенные запятыми) — текстовый формат, предназначенный для представления табличных данных. Каждая строка файла — это одна строка таблицы. Значения отдельных колонок отделяются друг от друга установленным в системе разделительным символом. В опубликованных на портале открытых данных используется разделитель точка с запятой.</p>
         </blockquote>
