        <center><h3>Ответы на часто задаваемые вопросы</h3></center>
        <p class="question"><strong>1. Что такое «Открытые данные»?</strong></p>
        <blockquote>
        <p><strong>Открытые государственные данные</strong>&nbsp;- это информация, содержащая сведения о деятельности государственных органов и размещенная в сети "Интернет" в виде систематизированных данных, организованных в формате, обеспечивающем ее автоматическую обработку без предварительного изменения человеком (машиночитаемый формат), в целях неоднократного, свободного и бесплатного использования.</p>
        </blockquote>
        <p class="question"><strong>2. Что такое паспорт открытых данных?</strong></p>
         <blockquote>
        <p>Паспорт открытых данных – совокупность сведений о наборе открытых данных, необходимых для установления факта принадлежности набора открытых данных к той или иной тематической рубрике, его потенциальной пригодности для решения задач потребителя, а также установления адреса размещения, способа загрузки и последующей автоматической обработки набора открытых данных.</p>
        </blockquote>
        <p class="question"><strong>3. Что такое реестр наборов открытых данных?</strong></p>
        <blockquote>
        <p>Реестр наборов открытых данных – систематизированный перечень наборов открытых данных, позволяющий в автоматическом режиме осуществлять навигацию по наборам открытых данных и получать ключевые параметры этих наборов, включая гиперссылки, необходимые для доступа к наборам открытых данных.</p>
        </blockquote>
        <p class="question"><strong>4. Какие типовые&nbsp;проблемы возникают при подготовки и публикации наборов?</strong></p>
        <blockquote>
            <p>Для подготовки файлов к публикации в формате CSV предъявляются 3 основных требования (см.&nbsp;<a href="http://data.gov.ru/tipovye-problemy-podgotovki-i-publikacii-naborov" target="blank">Портал открытых данных Российской Федерации</a>):</p>
        <ol>
        <li>Файл должен содержать данные в табличном представлении.</li>
        <li>Таблица должна иметь плоский вид (отсутствует какая-либо иерархия, в столбцах и строках – отсутствуют объединенные ячейки).</li>
        <li>Таблица не должна содержать заголовок и иную информацию. Таблица должна начинаться с первой ячейки.</li>
        </ol>
        </blockquote>
